t = Time.new
puts "Current time is:" + t.inspect
puts "Current year is:" + t.year.inspect
puts "Current month is:" + t.month.inspect
puts "Current day is:" + t.day.inspect
puts "Current week day is:" + t.wday.inspect
puts "Current year day is:" + t.yday.inspect
puts "Current hour is:" + t.hour.inspect
puts "Current minute is:" + t.min.inspect
puts "Current Second is:" + t.sec.inspect
# gives microseconds
puts "Current microseconds is:" + t.usec.inspect
# gives time zone
puts "Current zone is:" + t.zone.inspect